function setTextContentRetainingElements(el, newText) {
    // A simple method to replace only the text node children, ignoring elements like emojis or SVGs if they were separated
    // But since it's simple text replacements, standard textContent is mostly fine unless there's an icon.
    // If element contains <span> or <div> child elements, we assume the text is inside them and we find the right one, 
    // or just replace text nodes at root level.
    let textNodeFound = false;
    for (let i = 0; i < el.childNodes.length; i++) {
        const child = el.childNodes[i];
        if (child.nodeType === Node.TEXT_NODE && child.textContent.trim().length > 0) {
            child.textContent = newText;
            textNodeFound = true;
            break;
        }
    }
    if (!textNodeFound) {
       el.innerHTML = newText; // Fallback
    }
}

window.applyLanguage = function(lang) {
  if(!translations[lang]) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
        setTextContentRetainingElements(el, translations[lang][key]);
    }
  });
  
  document.querySelectorAll('input[placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (key && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });
  
  // Make sure all selectors represent current state
  document.querySelectorAll('.lang-selector').forEach(sel => {
      sel.value = lang;
  });
  
  localStorage.setItem('os_lang', lang);

  // If there's an ongoing render that is dependent on lang, dispatch an event
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang }}));
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('os_lang') || 'en';
    window.applyLanguage(savedLang);

    document.querySelectorAll('.lang-selector').forEach(sel => {
        sel.addEventListener('change', (e) => {
            window.applyLanguage(e.target.value);
            // Quick reload for complex dynamic views if they exist to re-render translated strings
            if(window.location.pathname.includes('dashboard-')) {
               // Soft rerender if switchView exists
               if(typeof switchView === 'function') {
                   const activeBtn = document.querySelector('.filter-btn.active');
                   if(activeBtn) {
                       // The timeout allows the translations dict to set first safely before generating new HTML
                       // But actually we are injecting HTML from JS in dashboard so those strings need to use translations[lang][key] directly.
                       // Instead of rewriting the whole app to use translation functions, we can just reload the page for dashboard.
                       window.location.reload(); 
                   }
               }
            }
        });
    });
});
